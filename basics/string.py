## Strings
# Strings are immutable sequences of characters enclosed in quotes.
str1 = "Hello, World!"
print(str1)  # Output: Hello, World!
# We can alse use single quotes
str2 = "Hello, World!"
# We can also use triple quotes for multi-line strings
str3 = """This is a multi-line string.
It can span multiple lines."""
print(str3)  # Output: This is a multi-line string. It can span multiple lines.


str = "learing python"
len_str = len(str)
print("Length of the string:", len_str)
str = "apple"
print(str[-3:-1])  # Slicing the string
